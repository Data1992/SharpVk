        }
        
        /// <summary>
        /// 
        /// </summary>
        public int X; 
        
        /// <summary>
        /// 
        /// </summary>
        public int Y; 
        
        /// <summary>
        /// 
        /// </summary>
        public int Z; 
    }
}
